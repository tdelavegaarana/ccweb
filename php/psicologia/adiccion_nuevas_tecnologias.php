  <h3 class = "verde">Psicología Clínica->Infanto Juvenil->Adicción a nuevas Tecnologías</h3>
  <p><strong>Lda. Vanessa Martín Muñoz, Nº Colegiado: CA-00697</strong></p>
  <p>Cada vez es más frecuente que los adolescentes o a veces niños en edad 
  escolar presenten problemas de abuso con las nuevas teconologías (móvil, 
  internet, chat whatsapp, etc).<br><br>Cuando esas activiadaes se convierten en 
  una prioridad para la persona, llegando a provocar un alejamiento de otras 
  actividades o personas hasta entonces importantes debemos sospechar que pueda 
  ser un comportamiento patológico. Otras manifestaciones pueden ser, 
  irritabilidad, discusiones frecuentes, dificultades para dormir, etc.</p>
