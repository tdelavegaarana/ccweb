  <p>Al igual que en la población adulta, las técnicas de relajación son una 
  herramienta clave para la adquisición de mecanismos de afrontamiento en 
  múltiples patologías (trastornos de ansiedad, del estado de ánimo, etc.), 
  aunque tambien como un elemento de apoyo para alcanzar un mayor bienestar 
  en nuestra vida cotidiana.<br><br>Existen técnicas adaptadas a la población 
  infantil. Los padres pueden ayudar a guiar y supervisar estas técnicas, y 
  con ellas además pueden reforzar y afianzar los vínculos afectivos creando 
  un espacion de interacción para su entrenamiento y posterior puesta en 
  práctica en los momentos necesarios.
