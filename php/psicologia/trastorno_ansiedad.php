  <p><strong>Lda. Vanessa Martín Muñoz, Nº Colegiado: CA-00697</strong></p>
  <p>
    La ansiedad es una respuesta que surge cuando la persona se siente amenazada o 
    en peligro, sea éste real o imaginado. Por tanto, se trata de una respuesta normal 
    y adaptativa que sirve para preparar al organismo para enfrentarse a una situación 
    de peligro. Al igual que en los adultos, en los niños, hay ocasiones en los que 
    la ansiedad aparece aunque no exista un peligro real, o que continúa, aún a pesar 
    de que la situación que provocó dicha respuesta ya ha desaparecido.<br><br>Los trastornos 
    más frecuentes en los niños son las FOBIAS ESPECÍFICAS (fobia a los animales, a 
    la oscuridad, a los ruidos intensos y fobia escolar), la ANSIEDAD DE SEPARACIÓN 
    y el RETRAIMIENTO SOCIAL.<br><br>Desde una orientación cognitivo-conductual y a través 
    de técnicas emocionales y basadas en las terapias de última generación (aceptación 
    y compromiso, mindfulness, procesamiento emocional, etc.) se trata de dar respuesta 
    en la terapia a estos trastornos.
  </p>
