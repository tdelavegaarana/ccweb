  <h3 class = "verde">Psicología Clínica->Infanto Juvenil->Trastornos de Conducta</h3>
  <p><strong>Lda. Vanessa Martín Muñoz, Nº Colegiado: CA-00697</strong></p>
  <p>
    La desobedencia y la agresividad son los comportamientos más habituales por 
    los que se consulta a un psicólogo infantil.<br><br>
    Con <strong>desobediencia</strong> nos estamos refiriendo a que el niño se 
    niega a hacer aquello que se le pide o bien hace aquello que se le indica 
    que no haga, bien sea porque se le pide de forma directa o porque se salta 
    una norma que habíamos establecido.<br><br>
    Son conductas que si se presentan con mucha frecuencia e intensidad pueden 
    producir un marcado deterioro en el funcionamiento familiar.<br><br>
    Existen técnicas psicológicas, de modificación de conducta útiles para elimniar 
    estos comportamientos y que se adquieran nuevas respuestas más adaptativas.<br><br>
    La <strong>conducta agresiva</strong> se refiere a que el niño puede mostrar 
    comportamientos destructivos, combativos, de crueldad, irritabilidad acusada, 
    desafio a la autoridad, necesidad de llamar la atención, etc. Si dichas conductas 
    ocurren en una edad en la que el niño ya tendría que haber aprendido otros 
    modos de responder a la frustración, o se da de forma demasiado frecuente e 
    intensa, produciendo interferencia en el ámbito familiar, es necesario 
    intervenir; a través de Técnicas de Modificación de Conducta se puede conseguir 
    una mejora en el comportamiento.
  </p>
