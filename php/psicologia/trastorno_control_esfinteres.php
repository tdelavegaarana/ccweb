  <h3 class = "verde">Psicología Clínica->Infanto Juvenil->Trastorno del Control de Esfínteres</h3>
  <p><strong>Lda. Vanessa Martín Muñoz, Nº Colegiado: CA-00697</strong></p>
  <p>A veces, en los niños, se produce un retraso en la adquisición del control 
  de esfínteres (enuresis, cuando tienen dificultades para orinar en el momento 
  y lugar adecuados; y encopresis, cuando el problema es la defecación).<br><br>En 
  muchos niños se resuelve según éstos alcanzan una maduración de su sistema 
  nervioso y con ello la capacidad para discriminar adecuadamente la percepción 
  de vejiga (o intestino) llena y aprenden a poner en marcha una serie de 
  conductas para eliminar la orina o heces en el lugar y momento oportuno.<br><br>Sin 
  embargo, otros niños tienen mayores dificultades y éstas pueden interferir en 
  otras áreas, como la familiar, social y/o escolar, con el consiguiente malestar 
  emocional en el niño.<br><br>Existen formas de facilitar este aprendizaje para los 
  niños y familias que lo necesitan.<br><br>A veces también sucede, que se ha 
  conseguido dicho aprendizaje, pero por otros motivos, por ejemplo emocionales 
  (llegada de un hermano, experiencia traumática, etc.), el/la niño/a vuelve a 
  presentar problemas de control de esfínteres.
  </p>
