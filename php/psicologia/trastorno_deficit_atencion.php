  <h3 class = "verde">Psicología Clínica->Infanto Juvenil->Trastorno por Déficit de Atención y/o Hiperactividad (TDAH)</h3>
  <p><strong>Lda. Vanessa Martín Muñoz, Nº Colegiado: CA-00697</strong></p>
  <p>Se estima que un 5% de los niños en edad escolar sufre este trastorno, siendo 
    más frecuente en niños que en niñas.<br><br>No existen datos concluyentes respecto 
    al origen de la hiperactividad, pero sí se conocen qué factores influyen en la 
    aparición de dicho trastorno, entre ellos, facotres biológicos, retraso madurativo, 
    factores pre y perinatales, influencias genéticas y variables ambientales.<br><br>Es 
    importante un adecuado diagnóstico, por parte de un clinico (psiquiatra, psicólogo 
    clínico).<br><br>Existen técnicas de modificación de conducta que pueden ayudar a los 
    padres a manejar los comportamientos inadecuados de los niños que sufren TDAH. 
    Además, el abordaje psicológico con el niño permite mejorar aspectos como el 
    autocontrol, la impulsividad, la autoestima, etc.<br><br>
    <ul>
      <li><u><strong>ESCUELA DE PADRES</strong></u> grupos de padres que se reúnen 
        semanal o quincenalmente durante un periodo de tiempo para aprender estrategias 
        para el manejo de las dificultades cotidianas en la convivencia y favorecer la 
        adaptación de todos los miembros de la familia. Es necesaria la implicación de 
        todo el contexto (socio-familiar) del niño con TDAH para superar las dificultades 
        de los niños y sus familias con ésta problemática.</li>
    </ul>
  </p>
